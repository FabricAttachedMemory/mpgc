// Things missing from GCC 4.8.1

#include "pheap_util.h"
#include <memory>

#if defined(_WIN32)
#include <Windows.h>
#else
#include <unistd.h>
#endif

using std::size_t;

// Kludgy implementation, but it should be implemented in the distribution
void* gcc_missing::align(size_t alignment, size_t size, void*& ptr, size_t& space) {
	char *area = static_cast<char *>(ptr);
	size_t offset = reinterpret_cast<size_t>(ptr) % alignment;
	size_t move = offset == 0 ? 0 : alignment - offset;
	size_t n = space-move;
	area += move;
	if (n < size) {
		return nullptr;
	}
	ptr = area;
	space = n;
	return ptr;
}

